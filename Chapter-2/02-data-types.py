# Data Types
# 1. Int
# 2. Float
# 3. Boolean
# 4. String
# 5. None

name = "Panchajanya"
age = 18
gpa = 9.6
is_programmer = True
is_musician = False
friends = None

# type() function is used to determine data type of any object.
print(name, type(name))
print(age, type(age))
print(gpa, type(gpa))
print(is_programmer, type(is_programmer))
print(is_musician, type(is_musician))
print(friends, type(friends))

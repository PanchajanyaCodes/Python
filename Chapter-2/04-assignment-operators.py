# Assignment Operators.
# 1. = Equals to
# 2. += Add and equal to
# 3. -= Subtract and equal to
# 4. *= Multiply and equal to
# 5. /= Divide and equal to
# 6. //= Floor Division and equal to
# 7. %= Modulus and equal to
# 8. **= Exponentiation and equal to

a = 10
print(a)

a += 1
print(a)

a -= 3
print(a)

a *= 3
print(a)

a /= 3
print(a)

a //= 3
print(a)

a **= 3
print(a)

a %= 2
print(a)

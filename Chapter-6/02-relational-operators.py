# Relational Operators.
# 1. == Equal to
# 2. > Greater than
# 3. >= Greater than or equal to
# 4. < Less than
# 5.<= Less than or equal to
# 6 != Not equal to

a = 10
b = 20

print(a == b)  # False
print(a > b)  # False
print(a >= b)  # False
print(a < b)  # True
print(a <= b)  # True
print(a != b)  # True
